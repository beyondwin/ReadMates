이 회차는 책 자체의 난이도와 추상성이 대화의 조건이 되었다. 네 사람 모두 이해의 어려움을 인정했고, 그 위에서 신념, 후회, 선택, 도덕, 명상, 운명을 붙잡으려 했다.

