이 회차는 소설 속 돌봄, 생활고, 가족, 범죄를 어디까지 이해할 수 있는지 묻는 자리였다.

대화는 "웃을 수 있었는가"라는 질문에서 시작했지만, 실제로는 몰입 실패, 도덕 판단, 사회 제도의 빈틈, 가족의 재정의를 다뤘다.

