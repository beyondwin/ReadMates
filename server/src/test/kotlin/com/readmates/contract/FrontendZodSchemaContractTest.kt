 * front/tests/unit/__fixtures__/zod-schemas/.
 *
 * Each fixture file is generated by `pnpm --dir front zod:export-fixtures` and represents
 * the expected key set of the corresponding zod-validated API response shape. If the server
 * adds or removes an object field without updating
 * the zod schema (and therefore the fixture), this test fails.
