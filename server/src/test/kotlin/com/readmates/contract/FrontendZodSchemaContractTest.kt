        @Test
        fun `recursive shape helper detects nested object key drift`() {
            val actual = objectMapper.readTree("""{"outer":{"serverOnly":true}}""")
            val expected = objectMapper.readTree("""{"outer":{"fixtureOnly":true}}""")

            assertThatThrownBy {
                assertJsonShapeMatches(
                    actual = actual,
                    expected = expected,
                    path = "$",
                    fixtureFileName = "inline-fixture.json",
                )
            }.isInstanceOf(AssertionError::class.java)
                .hasMessageContaining("JSON object key set at '$.outer'")
                .hasMessageContaining("serverOnly")
                .hasMessageContaining("fixtureOnly")
        }

        @Test
        fun `recursive shape helper detects object node type drift`() {
            val actual = objectMapper.readTree("""{"outer":null}""")
            val expected = objectMapper.readTree("""{"outer":{"field":true}}""")

            assertThatThrownBy {
                assertJsonShapeMatches(
                    actual = actual,
                    expected = expected,
                    path = "$",
                    fixtureFileName = "inline-fixture.json",
                )
            }.isInstanceOf(AssertionError::class.java)
                .hasMessageContaining("JSON node at '$.outer'")
                .hasMessageContaining("must be an object")
        }

        @Test
        fun `recursive shape helper detects missing representative array elements`() {
            val actual = objectMapper.readTree("""{"items":[]}""")
            val expected = objectMapper.readTree("""{"items":[{"field":true}]}""")

            assertThatThrownBy {
                assertJsonShapeMatches(
                    actual = actual,
                    expected = expected,
                    path = "$",
                    fixtureFileName = "inline-fixture.json",
                )
            }.isInstanceOf(AssertionError::class.java)
                .hasMessageContaining("JSON array at '$.items'")
                .hasMessageContaining("must contain at least one representative item")
        }

        @Test
        fun `recursive shape helper checks every actual array element`() {
            val actual = objectMapper.readTree("""{"items":[{"field":true},{"extra":true}]}""")
            val expected = objectMapper.readTree("""{"items":[{"field":true}]}""")

            assertThatThrownBy {
